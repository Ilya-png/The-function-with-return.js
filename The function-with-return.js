let greeting = "Hello";
let sum = 0;
let prod = 0;

function greet(value) {
  greeting = greeting + " " + value;
}

function sumOfTwo(a, b) {
  sum = a + b;
}

function multiply(a, b, c) {
  prod = a * b * c;
}

function sayHi(name) {
  return "Hello " + name + "!";
}

function returnWhatISay(value) {
  return value;
}

function divide(a, b) {
  return a / b;
}

function remainder(a, b) {
  return a % b;
}
